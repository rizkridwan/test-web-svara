/**
 * membuat sebuah Array dengan isian semua parameter
 * @param1 merah
 * @param2 kuning
 * @param3 hijau
 * @param4 biru
 *
 * @return sebuah @Array
 */
function membuatArray(param1, param2, param3, param4) {
  const arraySaya = []; //membuat variabel Array

  //tempat coding disini
  arraySaya.push(param1,param2,param3,param4);

  return arraySaya;
}
